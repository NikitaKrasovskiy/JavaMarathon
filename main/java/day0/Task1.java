package day0;

public class Task1 {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            System.out.print("JAVA" + " ");
            i++;
        }
        System.out.println();
        for (int j = 1; j < 10; j++) {
            System.out.print("JAVA" + " ");
        }

        for (int j = 0; j <= 10; j++) {
            System.out.println("JAVA");
        }
    }
}